package com.kp;

public class Calculator {

	/**
	 * Adding operation.
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int add(int a, int b) {
		return a + b;
	}
	
	/**
	 * This is a comment for subtract.
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int substract(int a, int b) {
		return a - b;
	}
}
