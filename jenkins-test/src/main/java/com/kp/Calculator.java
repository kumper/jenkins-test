package com.kp;

public class Calculator {
	
	/**
	 * Adding comment.
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int add(int a, int b) {
		return a + b;
	}
	
	/**
	 * Subtracting.
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int substract(int a, int b) {
		return a - b;
	}

}
